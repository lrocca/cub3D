#include "main.h"

void	draw_life(void)
{
	int	i;

	i = 0;
	while (i < g_cub.w * g_cub.plr.life / LIFE)
	{
		my_mlx_pixel_put(&g_cub.data, i, 0, 0xFFFFFF);
		i++;
	}
}
